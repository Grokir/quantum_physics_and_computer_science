from interface import QuantumDevice

def qrng(device: QuantumDevice) -> bool:
    """
    Генерирует случайный бит используя квантовый генератор случайных чисел.
    
    Алгоритм:
    1. Инициализируем кубит в состоянии |0⟩
    2. Применяем вентиль Адамара H: |0⟩ → (|0⟩ + |1⟩)/√2 (суперпозиция)
    3. Измеряем кубит: получаем 0 или 1 с вероятностью 50% каждый
    
    Возвращает:
        bool: случайный бит (0 или 1)
    """
    with device.using_qubit() as q:
        q.h()
        return q.measure()